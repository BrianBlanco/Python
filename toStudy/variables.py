myInt = 7

# Float, the value it's a fractional value
myFloat = 7.2

# String, it stores text
myString = "Hello World"

# Boolean, it can be True o false
myBoolean = True

# Array, it contains a list with all kind of data types
myArray = ["Brian", "Jose", 2, True]

