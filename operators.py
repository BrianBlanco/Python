# Sum
result = 1 + 2
print(result)

# Substract
result = 1 - 2
print(result)

# Multiplication
result = 2 * 2
print(result)

# Division
result = 2 / 2
print(result)

# Remainder
result = 10 % 3
print(result)

# Square
result = 2 ** 2
